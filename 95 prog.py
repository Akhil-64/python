def pattern(a,b=10,c=15):
    for i in range(0,a):
        print("%",end=" ")
    print()
    for i in range(0,a):
        print("a",end=" ")
    print()
    for i in range(0,b):
        print("a",end=" ")
    print()
    for i in range(0,c):
        print("a",end=" ")
    print()
    for i in range(0,c):
        print("a",end=" ")
    print()
    
pattern(7,6)
